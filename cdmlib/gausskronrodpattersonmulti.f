      SUBROUTINE AITKEN(SOFN,N,A,LARRAY,ESTLIM)
      implicit none
      integer N,LOWMAX,LARRAY,M,J
      double complex A,SOFN,ESTLIM,DENOM
      double precision huge,tiny, TWO                      
      DIMENSION A(0:LARRAY)
      PARAMETER ( HUGE = 1.d+60 , TINY = 1.d-60 , TWO = 2.d0 )
      A(N) = SOFN
      IF (N.LT.2) THEN
         ESTLIM = SOFN
      ELSE
         LOWMAX = N/2
         DO 10 J = 1,LOWMAX
            M = N - 2*J
            DENOM = A(M+2) - TWO*A(M+1) + A(M)
            IF (cdABS(DENOM).LT.TINY) THEN
               A(M) = HUGE
               write(*,*) 'pb avec integration essayer sans aitken'
               stop
            ELSE
               A(M) = A(M) - (A(M) - A(M+1))**2 / DENOM
            END IF
 10      CONTINUE
         IF ( MOD(N,2) .EQ. 0 ) THEN
            ESTLIM = A(0)
         ELSE
            ESTLIM = A(1)
         END IF
      END IF
      RETURN
      END
